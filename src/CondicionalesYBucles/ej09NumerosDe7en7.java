package CondicionalesYBucles;

public class ej09NumerosDe7en7 {

	public static void main(String[] args) {
	//escribira los numeros restandole cada vez 7
	//usaremos un for para que repita esta accion hasta que llegue al 0
		int i;
		for (i=100; i>=0; i-=7) {
			System.out.println(i);
			System.out.println();
			
		}


	}

}
